extension N {
    func getNote(from staffPosition: Int, and relativePosition: Int, double: Bool = false) -> [Note] {
        var array: [Note]
        if double {
            array = N.usefulNotes.filter { $0.staffPosition == staffPosition && $0.relativePosition == relativePosition }
        } else {
            array = N.singleAccUsefulNotes.filter { $0.staffPosition == staffPosition && $0.relativePosition == relativePosition }
        }
        return array
    }
    
    func getNoteBy(relativePosition: Int, double: Bool = false) -> [Note] {
        var array: [Note]
        if double {
            array = N.usefulNotes.filter { $0.relativePosition == relativePosition }
        } else {
            array = N.singleAccUsefulNotes.filter { $0.relativePosition == relativePosition }
        }
        return array
    }
    
    func getNoteBy(staffPosition: Int, double: Bool = false) -> [Note] {
        var array: [Note]
        if double {
            array = N.usefulNotes.filter { $0.staffPosition == staffPosition }
        } else {
            array = N.singleAccUsefulNotes.filter { $0.staffPosition == staffPosition }
        }
        return array
    }
